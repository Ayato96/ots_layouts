        			<h3>Sobre o Servidor</h3>
        			<p>Um projeto de Open Tibia criado e administrado por uma equipe seria e competente. Trata-se de um servidor focado em RPG low rate (exp baixa) com mapa, sistemas, quests e eventos próprios visando a gameplay e diversão. Através do Thoria podemos trazer pra você um servidor único, porém mantendo a essência do Tibia.</p>
        			<p>Nós trazemos o Thoria, com uma equipe ainda mais experiente e séria, mapas e sistemas reformulados e reescritos, e uma versão mais atual, a 10.00. A expectativa é termos um servidor estável com compromisso aos jogadores e a comunidade, nossa equipe está motivada a criar um ambiente agradável a todos os jogadores, com desafios e uma excelente gameplay.</p>
        			<p>Esperamos que possa fazer parte dessa comunidade, estamos mais prontos do que nunca, mais experientes, e queremos ouvir você para juntos fazermos um servidor repleto de desafios e diversão para todos nós.</p><br>
        			<h3>Sobre o Thoria Wikia</h3>
        			<p>Nosso wikia veio pra clarear a mente daqueles que querem saber mais sobre os desafios e aventuras que vão enfrentar no mundo de Thoria, com tudo que o servidor possui.</p>
        			<p>No menu ao lado você encontrará opções onde poderá encontrar algumas dicas que lhe ajudarão a desvendar muita das coisas que possuimos no servidor, por exemplo: principais quests e lugares de caça, sistemas, eventos, algumas particularidades e muitas outras coisas.</p>
        			<p>Sempre fique atento ao nosso wikia, pois podemos estar incluindo ou modificando algumas coisas aqui. Estaremos constantemente trabalhando para que nosso wikia fique muito informativo para o players novos e alguns outros que estão por fora das novidades que o Thoria está trazendo.</p><br>
